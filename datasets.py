def diffimage(src_1,src_2):#帧差法
    #src_1=np.array(src_1)
    #src_2=np.array(src_2)
    src_1 = src_1.astype(np.int)
    src_2 = src_2.astype(np.int)
    diff = abs(src_1 - src_2)
    return diff.astype(np.uint8)

